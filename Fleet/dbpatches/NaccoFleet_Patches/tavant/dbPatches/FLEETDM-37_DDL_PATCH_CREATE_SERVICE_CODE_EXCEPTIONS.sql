CREATE TABLE SERVICE_CODE_EXCEPTIONS
  (
    ID 			NUMBER(19,0) NOT NULL ENABLE,
    START_DATE 		DATE,
    END_DATE 		DATE,
    DEALER 		NUMBER(19,0),
    DURATION 		FLOAT(126),
    DESCRIPTION 	VARCHAR2(255 CHAR),
    CODE        	VARCHAR2(255 CHAR),
    CONSTRAINT SERVICE_CODE_EXCEPTIONS_PK PRIMARY KEY (ID),
    CONSTRAINT SERVICE_CODE_DLR_FK FOREIGN KEY (DEALER) REFERENCES SERVICE_PROVIDER (ID) ENABLE
  )
/
CREATE SEQUENCE SERVICE_CODE_EXCPTNS_SEQ MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 20 START WITH 110000000001240 CACHE 20 NOORDER NOCYCLE
