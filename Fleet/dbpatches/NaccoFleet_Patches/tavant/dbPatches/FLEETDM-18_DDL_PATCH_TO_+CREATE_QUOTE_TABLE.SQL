CREATE TABLE QUOTE
  (
    ID           NUMBER(19,0) NOT NULL ENABLE,
    VERSION      NUMBER(10,0) NOT NULL ENABLE,
    QUOTE_NUMBER VARCHAR2(255 CHAR),
    FILED_ON_DATE DATE,
    LAST_UPDATED_BY  NUMBER(19,0),
    QUOTE_CREATED_BY NUMBER(19,0),
    FOR_DEALER       NUMBER(19,0),
    INSPECTION_DATE DATE,
    HOURS_ON_TRUCK      NUMBER,
    ACTIVE_QUOTE_AUDIT  NUMBER(19,0),
    BUSINESS_UNIT_INFO  VARCHAR2(255 CHAR),
    APPLICABLE_CONTRACT NUMBER(19,0),
    SERVICE_REQUEST     NUMBER(19,0),
    D_CREATED_ON DATE,
    D_INTERNAL_COMMENTS VARCHAR2(255 CHAR),
    D_UPDATED_ON DATE,
    D_LAST_UPDATED_BY NUMBER(19,0),
    D_CREATED_TIME TIMESTAMP (6),
    D_UPDATED_TIME TIMESTAMP (6),
    D_ACTIVE                NUMBER(1,0),
    CUSTOMER_QUOTE_RECEIVED VARCHAR2(20 BYTE) DEFAULT 0,
    CONSTRAINT QUOTE_PK PRIMARY KEY (ID) ENABLE
  )
/
CREATE SEQUENCE QUOTE_SEQ MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 20 START WITH 100000000011600
