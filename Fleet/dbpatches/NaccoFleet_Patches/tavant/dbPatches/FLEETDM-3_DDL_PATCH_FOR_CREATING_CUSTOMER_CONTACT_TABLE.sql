CREATE TABLE CUSTOMER_CONTACT
  (
    ID         NUMBER,
    FIRST_NAME VARCHAR2(255 CHAR),
    LAST_NAME  VARCHAR2(255 CHAR),
    DEPARTMENT VARCHAR2(255 CHAR),
    TITLE      VARCHAR2(255 CHAR),
    PHONE      NUMBER(19,0),
    FAX        NUMBER(19,0),
    EMAIL      VARCHAR2(255 CHAR),
    CONSTRAINT CUSTOMER_CONTACT_PK PRIMARY KEY (ID) ENABLE
  )
/

CREATE SEQUENCE CUSTOMER_CONTACT_SEQ MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 20 START WITH 100000000006800 CACHE 20 NOORDER NOCYCLE
