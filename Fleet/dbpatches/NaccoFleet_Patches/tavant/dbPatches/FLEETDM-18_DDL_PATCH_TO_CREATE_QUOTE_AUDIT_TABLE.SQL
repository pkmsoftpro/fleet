CREATE TABLE QUOTE_AUDIT
  (
    ID         NUMBER(19,0) NOT NULL ENABLE,
    VERSION    NUMBER(10,0) NOT NULL ENABLE,
    UPDATED_ON NUMBER(19,0),
    UPDATED_BY NUMBER(19,0),
    UPDATED_TIME TIMESTAMP (6),
    QUOTE_STATE         VARCHAR2(255 CHAR),
    ASSIGN_TO_USER      NUMBER(19,0),
    WORK_ORDER_NUMBER   VARCHAR2(4000 CHAR),
    ADDITIONAL_NOTES    VARCHAR2(4000 CHAR),
    FAULT               VARCHAR2(4000 CHAR),
    CAUSE               VARCHAR2(4000 CHAR),
    PROPOSED_CORRECTION VARCHAR2(4000 CHAR),
    WORK_PERFORMED      VARCHAR2(4000 CHAR),
    INTERNAL_COMMENTS   VARCHAR2(4000 BYTE),
    EXTERNAL_COMMENTS   VARCHAR2(4000 BYTE),
    FOR_QUOTE           NUMBER(19,0),
    LIST_INDEX          NUMBER(10,0),
    SERVICE_INFORMATION NUMBER(19,0),
    CONSTRAINT QUOTE_AUDIT_PK PRIMARY KEY (ID) ENABLE
  )
/
CREATE SEQUENCE QUOTE_AUDIT_SEQ MINVALUE 1 MAXVALUE 999999999999999999999999999 INCREMENT BY 20 START WITH 100000000011200
